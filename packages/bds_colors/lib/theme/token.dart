/// Tokens for [ColorTheme].
enum Token {
  /// Default page background.
  background,

  /// Hover state color for [background].
  backgroundHover,

  /// Active state color for [background].
  backgroundActive,

  /// Selected state color for [background].
  backgroundSelected,

  /// Hover state color for [backgroundSelected].
  backgroundSelectedHover,

  /// Feature background color.
  backgroundBrand,

  /// High contrast background color.
  backgroundInverse,

  /// Hover state color for [backgroundInverse].
  backgroundInverseHover,

  /// Primary text color.
  textPrimary,

  /// Secondary text color.
  textSecondary,

  /// Placeholder text.
  textPlaceholder,

  /// Text on interactive colors.
  textOnColor,

  /// Tertiary text color.
  textHelper,

  /// Error message text.
  textError,

  /// Text color on [backgroundInverse].
  textInverse,

  /// Disabled text color.
  textDisabled,

  /// Disabled text color for [textOnColor].
  textOnColorDisabled,
}
